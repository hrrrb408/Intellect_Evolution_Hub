def sync_fusion_proposal_statuses(report: dict[str, object], drafts: list[dict[str, object]], write_results: dict[str, str]) -> None:
    draft_by_target = {str(draft["target_path"]): draft for draft in drafts}
    for proposal in report["proposals"]:
        target = str(proposal["target_path"])
        if target in write_results:
            proposal["status"] = write_results[target]
            continue
        draft = draft_by_target.get(target)
        if draft and draft.get("apply_status") == "manual-review-existing-note":
            proposal["status"] = "manual-review-existing-note"


def append_moc_links(vault: Path, moc_rel: str, proposals: list[dict[str, object]]) -> int:
    moc = vault / moc_rel
    if not moc.exists():
        return 0
    text = safe_read(moc)
    added = 0
    for proposal in proposals:
        action = str(proposal.get("action") or "")
        target = str(proposal.get("target_path") or "")
        if action not in {"ensure_concept", "ensure_entity", "ensure_query", "ensure_source_summary"}:
            continue
        link = f"- {path_to_wikilink(target)}"
        if link in text:
            continue
        heading = {
            "ensure_concept": "Concepts",
            "ensure_entity": "Entities",
            "ensure_query": "Current Questions",
            "ensure_source_summary": "Source Summaries",
        }[action]
        text = append_under_heading(text, heading, link)
        added += 1
    if added:
        write_text(moc, text)
    return added


