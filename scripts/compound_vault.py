def build_index(vault: Path) -> list[NoteInfo]:
    ensure_scaffold(vault)
    notes = [analyze_note(vault, p) for p in sorted(iter_markdown(vault, include_generated=False), key=lambda x: rel(vault, x).lower())]
    by_type: dict[str, list[NoteInfo]] = defaultdict(list)
    for n in notes:
        by_type[n.note_type].append(n)

    lines = [
        "---",
        "title: Compound Vault Index",
        "type: index",
        f"updated: {now().strftime(DATETIME_FMT)}",
        "ai-first: true",
        "---",
        "",
        "## For future Claude",
        "",
        "This generated index maps vault notes by type, path, links, tags, and summaries. Read it after `wiki/hot.md`, then open only the relevant source notes.",
        "",
        "# Compound Vault Index",
        "",
        GENERATED_MARKER,
        "",
        "> Retrieval rule: read [[hot]] first, then this index, then only the most relevant notes.",
        "",
        "## Stats",
        "",
        f"- Total markdown notes: {len(notes)}",
        f"- Generated at: {now().strftime(DATETIME_FMT)}",
        "",
        "## Type Summary",
        "",
    ]
    for typ in sorted(by_type):
        lines.append(f"- {typ}: {len(by_type[typ])}")
    lines += ["", "## Notes", ""]
    for typ in sorted(by_type):
        lines += [f"### {typ}", ""]
        for n in sorted(by_type[typ], key=lambda x: x.path.lower()):
            link = path_to_wikilink(n.path)
            summary = f" - {n.summary}" if n.summary else ""
            tags = f" - tags: {', '.join(n.tags[:8])}" if n.tags else ""
            lines.append(f"- {link} (`{n.path}`){summary}{tags}")
        lines.append("")
    write_text(vault / "wiki/index.md", "\n".join(lines).rstrip() + "\n")
    write_text(vault / "wiki/meta/index.json", json.dumps([asdict(n) for n in notes], ensure_ascii=False, indent=2))
    return notes


def path_to_wikilink(path: str) -> str:
    stem = Path(path).stem
    if stem.lower() in {"index", "hot", "log"} and path.startswith("wiki/"):
        stem = Path(path).with_suffix("").as_posix()
    return f"[[{stem}]]"


