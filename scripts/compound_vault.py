def is_safe_patch_target_path(path: str) -> bool:
    normalized = Path(path).as_posix().lstrip("/")
    if normalized.startswith("../") or "/../" in normalized:
        return False
    unsafe_prefixes = ("wiki/sources/", "wiki/meta/", ".vault-meta/", ".git/", ".obsidian/")
    return normalized.endswith(".md") and not normalized.startswith(unsafe_prefixes)


def resolve_patch_target(vault: Path, target_path: str) -> Path | None:
    if not is_safe_patch_target_path(target_path):
        return None
    target = (vault / target_path).resolve()
    try:
        target.relative_to(vault.resolve())
    except ValueError:
        return None
    return target


def append_under_heading(text: str, heading: str, item: str) -> str:
    lines = text.rstrip().splitlines()
    heading_line = f"## {heading}"
    try:
        idx = lines.index(heading_line)
    except ValueError:
        return text.rstrip() + f"\n\n{heading_line}\n\n{item}\n"

    insert_at = len(lines)
    for i in range(idx + 1, len(lines)):
        if lines[i].startswith("## "):
            insert_at = i
            break
    block = ["", item]
    while insert_at > 0 and lines[insert_at - 1] == "":
        insert_at -= 1
    lines[insert_at:insert_at] = block
    return "\n".join(lines).rstrip() + "\n"


def analyze_note(vault: Path, path: Path) -> NoteInfo:
    text = safe_read(path)
    fm = extract_frontmatter(text)
    links = sorted(set(x.strip() for x in WIKILINK_RE.findall(text) if x.strip()))
    tags = sorted(set(re.findall(r"(?<!\w)#([A-Za-z0-9_\-/\u4e00-\u9fff]+)", text)))
    st = path.stat()
    return NoteInfo(
        path=rel(vault, path),
        title=fm.get("title") or extract_title(path, text),
        note_type=fm.get("type") or infer_note_type(vault, path),
        mtime=dt.datetime.fromtimestamp(st.st_mtime).astimezone().strftime(DATETIME_FMT),
        size=st.st_size,
        links=links,
        tags=tags,
        summary=extract_summary(text),
        content_hash=file_hash(text),
    )


def infer_note_type(vault: Path, path: Path) -> str:
    r = rel(vault, path)
    if r.startswith("raw/articles/") or r.startswith("raw/papers/") or r.startswith("raw/transcripts/"):
        return "source"
    if r.startswith("source-summaries/"):
        return "source-summary"
    if r.startswith("concepts/"):
        return "concept"
    if r.startswith("comparisons/"):
        return "comparison"
    if r.startswith("entities/"):
        return "entity"
    if r.startswith("queries/"):
        return "question"
    if r.startswith("mocs/"):
        return "moc"
    if r.startswith("maintenance/"):
        return "maintenance"
    if r.startswith("wiki/sources/"):
        return "source"
    if r.startswith("wiki/entities/"):
        return "entity"
    if r.startswith("wiki/concepts/"):
        return "concept"
    if r.startswith("wiki/projects/") or r.startswith("projects/"):
        return "project"
    if r.startswith("wiki/decisions/") or r.startswith("decisions/"):
        return "decision"
    if r.startswith("daily/") or r.startswith("journals/"):
        return "daily"
    if r.startswith("wiki/meta/"):
        return "meta"
    if r in {"wiki/index.md", "wiki/hot.md", "wiki/log.md"}:
        return "system"
    return "note"


def first_nonempty_lines(text: str, limit: int = 8) -> list[str]:
    lines = []
    for raw_line in strip_frontmatter(text).splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or line.startswith("---"):
            continue
        if line.startswith("!") or line.startswith("```"):
            continue
        lines.append(line)
        if len(lines) >= limit:
            break
    return lines


def write_source_summary(
    vault: Path,
    source_rel: str,
    title: str,
    source_text: str,
    domain: str = "engineering",
    subdomain: str = "ai-engineering",
    original_rel: str = "",
) -> str:
    if not is_singularity_mode(vault):
        return ""
    path = vault / route_path(vault, "source-summary", title, domain=domain, subdomain=subdomain)
    if path.exists():
        return rel(vault, path)
    excerpt_lines = first_nonempty_lines(source_text, limit=6)
    excerpt = "\n".join(f"> {line[:320]}" for line in excerpt_lines) if excerpt_lines else "> No readable excerpt extracted yet."
    signals = top_signal_terms(source_text)
    signal_line = ", ".join(signals) if signals else "(none yet)"
    raw_link = path_to_wikilink(source_rel)
    source_entries = [f"  - {source_rel}"]
    if original_rel:
        source_entries.insert(0, f"  - {original_rel}")
    source_yaml = "\n".join(source_entries)
    original_line = f"- Original file: `{original_rel}`" if original_rel else "- Original file: same as raw source note"
    body = textwrap.dedent(f"""\
    ---
    title: "{safe_title(title).replace('"', "'")}"
    type: source-summary
    date: {today()}
    updated: {today()}
    domain: {domain}
    subdomain: {subdomain}
    confidence: low
    ai-first: true
    sources:
    {source_yaml}
    ---

    ## For future Claude
    This single-source summary was generated during IEH singularity-mode ingest. Treat it as a reading scaffold, then replace low-confidence bullets with human-checked synthesis after reading the raw source and original file.

    # {safe_title(title)}

    ## Source Trail

    - Raw source note: {raw_link}
    - Raw source path: `{source_rel}`
    {original_line}

    ## Routing

    - Domain: `{domain}`
    - Subdomain: `{subdomain}`
    - Signal terms: {signal_line}

    ## Initial Reading Judgment

    - What problem does this source address?
    - What claims, definitions, methods, or entities should become durable pages?
    - What should be linked into the relevant MOC or query page?
    - Is this source primarily raw material, a paper, a transcript, a study note, or a decision artifact?

    ## Extracted Opening

    {excerpt}

    ## Follow-up

    - [ ] Confirm the title and source identity.
    - [ ] Write a concise Chinese-first summary.
    - [ ] Extract reusable concepts and entities.
    - [ ] Create a query page if the source is part of a learning or research direction.
    - [ ] Add this source to the relevant query page and MOC.
    """).rstrip() + "\n"
    write_text(path, body)
    return rel(vault, path)


