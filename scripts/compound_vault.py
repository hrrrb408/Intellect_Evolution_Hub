def mode_config(vault: Path) -> dict[str, object]:
    cfg = load_json(mode_path(vault), {})
    mode = cfg.get("mode", "generic") if isinstance(cfg, dict) else "generic"
    if mode not in VALID_MODES:
        mode = "generic"
    return {"mode": mode, "configured_at": cfg.get("configured_at") if isinstance(cfg, dict) else None}


def set_mode(vault: Path, mode: str) -> dict[str, object]:
    if mode not in VALID_MODES:
        raise ValueError(f"invalid mode: {mode}")
    payload = {
        "schema_version": 1,
        "mode": mode,
        "configured_at": now().isoformat(),
    }
    save_json(mode_path(vault), payload)
    return payload


