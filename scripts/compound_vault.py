def read_url(url: str, timeout: int = 20, max_bytes: int = 2_000_000) -> tuple[str, str]:
    req = urllib.request.Request(url, headers={"User-Agent": "obsidian-second-brain-plus/1.0"})
    with urllib.request.urlopen(req, timeout=timeout) as resp:
        content_type = resp.headers.get("content-type", "")
        data = resp.read(max_bytes + 1)
        if len(data) > max_bytes:
            data = data[:max_bytes]
    charset = "utf-8"
    m = re.search(r"charset=([^;]+)", content_type)
    if m:
        charset = m.group(1).strip()
    try:
        text = data.decode(charset)
    except Exception:
        text = data.decode("utf-8", errors="replace")
    return text, content_type


def normalize_source_text(raw: str, source: str, content_type: str = "") -> str:
    # Lightweight HTML cleanup, not a full parser; good enough for today-use ingestion.
    text = raw
    if "html" in content_type or "<html" in raw[:1000].lower():
        text = re.sub(r"(?is)<script.*?</script>", "", text)
        text = re.sub(r"(?is)<style.*?</style>", "", text)
        title = re.search(r"(?is)<title[^>]*>(.*?)</title>", text)
        title_text = re.sub(r"\s+", " ", title.group(1)).strip() if title else source
        text = re.sub(r"(?is)<br\s*/?>", "\n", text)
        text = re.sub(r"(?is)</p\s*>", "\n\n", text)
        text = re.sub(r"(?is)<[^>]+>", " ", text)
        text = re.sub(r"\n{3,}", "\n\n", text)
        text = re.sub(r"[ \t]{2,}", " ", text)
        text = f"# {title_text}\n\n{text.strip()}"
    return text.strip()


def cmd_ingest(args: argparse.Namespace) -> int:
    vault = resolve_vault(args.vault)
    ensure_scaffold(vault)
    src = args.source
    source_kind = "url" if re.match(r"https?://", src) else "file"
    content_type = ""
    original_rel = ""
    is_pdf_source = False
    if source_kind == "url":
        raw, content_type = read_url(src)
        base = slugify(src)
        is_pdf_source = "pdf" in content_type.lower() or urllib.parse.urlparse(src).path.lower().endswith(".pdf")
    else:
        p = Path(src).expanduser().resolve()
        if not p.exists():
            print(f"Source not found: {p}", file=sys.stderr)
            return 2
        base = slugify(p.stem)
        is_pdf_source = p.suffix.lower() == ".pdf"
        if is_pdf_source:
            pdf_bytes = p.read_bytes()
            digest = bytes_hash(pdf_bytes)
            title_for_pdf = safe_title(p.stem)
            domain, subdomain = infer_domain_subdomain(title_for_pdf, "", src)
            original_rel = copy_original_pdf(vault, p, domain, subdomain, title_for_pdf)
            raw = paper_sidecar_text(p, digest)
        else:
            raw = safe_read(p, limit_bytes=args.max_bytes)
    text = normalize_source_text(raw, src, content_type)
    title = extract_title(Path(base + ".md"), text) or base
    domain, subdomain = infer_domain_subdomain(title, text, src) if is_singularity_mode(vault) else ("", "")
    if source_kind == "url" and is_pdf_source:
        digest = file_hash(text)
        title_for_pdf = safe_title(title)
        original_rel = f"external-pdf:{src}"
    elif not is_pdf_source:
        digest = file_hash(text)
    manifest = load_manifest(vault)
    source_key = f"{source_kind}:{src}"
    existing = manifest["sources"].get(source_key)
    if existing and existing.get("content_hash") == digest and not args.force:
        print(f"Already ingested unchanged source: {existing.get('source_note')}")
        print("Use --force to re-ingest.")
        return 0
    routed = route_path(vault, "source", f"{today()}-{base}-{digest[:8]}", domain=domain or None, subdomain=subdomain or None)
    out = vault / routed
    ingested_at = now().strftime(DATETIME_FMT)
    if is_singularity_mode(vault):
        checklist = [
            "- [ ] Confirm this belongs under the inferred domain/subdomain.",
            "- [ ] If this is a PDF, verify the preserved original under `raw/papers/`.",
            "- [ ] Complete the matching `source-summaries/` page.",
            "- [ ] Extract entities into `entities/`.",
            "- [ ] Extract concepts or comparisons into `concepts/` or `comparisons/`.",
            "- [ ] Create or update a learning question in `queries/`.",
            "- [ ] Update the relevant `mocs/` page.",
            "- [ ] Flag contradictions or stale claims before applying proposals.",
        ]
    else:
        checklist = [
            "- [ ] Extract entities into `wiki/entities/`",
            "- [ ] Extract concepts into `wiki/concepts/`",
            "- [ ] Link related projects/decisions",
            "- [ ] Flag contradictions or stale claims",
        ]
    body = "\n".join([
        "---",
        f'title: "{title.replace(chr(34), chr(39))}"',
        "type: source",
        f"date: {today()}",
        *( [f"domain: {domain}", f"subdomain: {subdomain}"] if is_singularity_mode(vault) else [] ),
        f'source: "{src.replace(chr(34), chr(39))}"',
        f"source_kind: {source_kind}",
        f"source_format: {'pdf' if is_pdf_source else 'text'}",
        *( [f"original_file: {original_rel}"] if original_rel else [] ),
        f"ingested: {ingested_at}",
        f"content_hash: {digest}",
        "status: raw-ingested",
        "ai-first: true",
        "---",
        "",
        "## For future Claude",
        f"This source note preserves raw material ingested from `{src}`. Extract durable entities, concepts, projects, decisions, and contradictions from it before relying on it as synthesized knowledge.",
        "",
        f"# {title}",
        "",
        "## Source",
        "",
        f"- Original: {src}",
        *( [f"- Preserved original: `{original_rel}`"] if original_rel else [] ),
        *( [f"- Inferred route: `{domain}/{subdomain}`"] if is_singularity_mode(vault) else [] ),
        f"- Ingested: {ingested_at}",
        f"- Hash: `{digest}`",
        "",
        "## Future Claude extraction checklist",
        "",
        *checklist,
        "",
        "## Raw Content",
        "",
        text,
    ]).rstrip() + "\n"
    write_text(out, body)
    source_rel = rel(vault, out)
    summary_rel = write_source_summary(vault, source_rel, title, text, domain=domain or "engineering", subdomain=subdomain or "ai-engineering", original_rel=original_rel)
    distributed = distribute_ingest(vault, source_rel, text) if args.distribute else {"entities": [], "concepts": []}
    build_index(vault)
    build_chunk_index(vault)
    analysis = write_analysis_artifacts(vault, source_rel, text, distributed) if args.analyze_claims else {}
    rewrite_plan = write_rewrite_plan(vault, source_rel, text, distributed, analysis) if args.rewrite_plan else ""
    manifest["sources"][source_key] = {
        "source": src,
        "source_kind": source_kind,
        "content_hash": digest,
        "ingested_at": now().strftime(DATETIME_FMT),
        "source_note": source_rel,
        "source_summary": summary_rel,
        "source_format": "pdf" if is_pdf_source else "text",
        "original_file": original_rel,
        "domain": domain,
        "subdomain": subdomain,
        "distributed": distributed,
        "rewrite_plan": rewrite_plan,
        "analysis": analysis.get("paths", {}) if isinstance(analysis, dict) else {},
    }
    save_manifest(vault, manifest)
    build_index(vault)
    build_chunk_index(vault)
    update_hot(vault, limit=args.hot_limit)
    claim_count = len(analysis.get("claims", [])) if isinstance(analysis, dict) else 0
    contradiction_count = len(analysis.get("contradictions", [])) if isinstance(analysis, dict) else 0
    proposal_count = len(analysis.get("patch_proposals", [])) if isinstance(analysis, dict) else 0
    log_event(vault, "compound-ingest", f"{src} -> {source_rel}; summary={summary_rel or 'none'}; entities={len(distributed['entities'])}; concepts={len(distributed['concepts'])}; claims={claim_count}; contradictions={contradiction_count}; proposals={proposal_count}; rewrite_plan={rewrite_plan or 'none'}")
    print(f"Ingested source into {out}")
    if summary_rel:
        print(f"Source summary: {vault / summary_rel}")
    if rewrite_plan:
        print(f"Rewrite plan: {vault / rewrite_plan}")
    if analysis:
        print(f"Claims: {claim_count}; contradictions: {contradiction_count}; patch proposals: {proposal_count}")
    if distributed["entities"] or distributed["concepts"]:
        print(json.dumps(distributed, ensure_ascii=False, indent=2))
    return 0


@dataclass
class SearchHit:
    path: str
    title: str
    score: float
    matched_terms: list[str]
    summary: str
    snippet: str
    chunk_id: str = ""
    rerank_score: float = 0.0
    rerank_source: str = "bm25-file"


def load_docs(vault: Path) -> list[tuple[Path, str, NoteInfo]]:
    docs = []
    for p in iter_markdown(vault, include_generated=False):
        try:
            text = safe_read(p)
            docs.append((p, text, analyze_note(vault, p)))
        except Exception:
            continue
    return docs


def chunk_text(text: str, target_chars: int = CHUNK_TARGET_CHARS, overlap: int = CHUNK_OVERLAP_CHARS) -> list[str]:
    body = strip_frontmatter(text).strip()
    if not body:
        return []
    paragraphs = re.split(r"\n\s*\n", body)
    chunks: list[str] = []
    current = ""
    for para in paragraphs:
        para = para.strip()
        if not para:
            continue
        if current and len(current) + len(para) + 2 > target_chars:
            chunks.append(current.strip())
            tail = current[-overlap:] if overlap > 0 else ""
            current = (tail + "\n\n" + para).strip()
        else:
            current = (current + "\n\n" + para).strip() if current else para
    if current:
        chunks.append(current.strip())
    return chunks


def build_chunk_index(vault: Path) -> dict[str, object]:
    ensure_scaffold(vault)
    meta = meta_dir(vault)
    chunks_root = meta / "chunks"
    bm25_root = meta / "bm25"
    with file_lock(meta / "chunk-index.lock"):
        if chunks_root.exists():
            shutil.rmtree(chunks_root)
        chunks_root.mkdir(parents=True, exist_ok=True)
        bm25_root.mkdir(parents=True, exist_ok=True)

        docs = {}
        df = Counter()
        postings: dict[str, list[list[object]]] = defaultdict(list)
        chunk_count = 0

        for path in sorted(iter_markdown(vault, include_generated=False), key=lambda p: rel(vault, p).lower()):
            try:
                text = safe_read(path)
                info = analyze_note(vault, path)
            except Exception:
                continue
            page_hash = file_hash(rel(vault, path))
            page_dir = chunks_root / page_hash
            page_dir.mkdir(parents=True, exist_ok=True)
            for idx, raw_chunk in enumerate(chunk_text(text)):
                body_hash = file_hash(raw_chunk)
                chunk_id = f"{page_hash}:{idx}"
                contextualized = f"{info.title}\n{info.note_type}\n{info.summary}\n\n{raw_chunk}"
                chunk_payload = {
                    "chunk_id": chunk_id,
                    "page_address": page_hash,
                    "page_path": rel(vault, path),
                    "page_title": info.title,
                    "chunk_index": idx,
                    "raw_text": raw_chunk,
                    "contextualized_text": contextualized,
                    "body_hash": body_hash,
                }
                chunk_path = page_dir / f"chunk-{idx:03d}.json"
                write_text(chunk_path, json.dumps(chunk_payload, ensure_ascii=False, indent=2) + "\n")
                tokens = tokenize(contextualized)
                tf = Counter(tokens)
                docs[chunk_id] = {
                    "path": rel(vault, chunk_path),
                    "page_path": rel(vault, path),
                    "page_title": info.title,
                    "chunk_index": idx,
                    "dl": len(tokens),
                }
                for term, count in tf.items():
                    df[term] += 1
                    postings[term].append([chunk_id, count])
                chunk_count += 1

        avg_dl = (sum(doc["dl"] for doc in docs.values()) / len(docs)) if docs else 0
        index = {
            "schema_version": 1,
            "params": {"k1": 1.5, "b": 0.75},
            "doc_count": len(docs),
            "chunk_count": chunk_count,
            "avg_dl": avg_dl,
            "updated_at": now().strftime(DATETIME_FMT),
            "vocab": {
                term: {"df": df[term], "postings": postings[term]}
                for term in sorted(df)
            },
            "docs": docs,
        }
        save_json(bm25_root / "index.json", index)
        return index


def load_chunk_payload(vault: Path, rel_path: str) -> dict[str, object] | None:
    path = vault / rel_path
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return None


def query_chunk_index(
    vault: Path,
    query: str,
    top_k: int = 8,
    bm25_top: int = 24,
    rerank_mode: str = "auto",
    allow_remote_ollama: bool = False,
) -> list[SearchHit]:
    index = load_json(meta_dir(vault) / "bm25" / "index.json", None)
    if not index or not index.get("docs"):
        return []
    q_terms = tokenize(query)
    if not q_terms:
        return []
    vocab = index["vocab"]
    docs = index["docs"]
    avg_dl = index.get("avg_dl") or 1.0
    n_docs = index.get("doc_count") or len(docs) or 1
    k1 = index.get("params", {}).get("k1", 1.5)
    b = index.get("params", {}).get("b", 0.75)
    scores: dict[str, float] = defaultdict(float)
    matched: dict[str, set[str]] = defaultdict(set)
    for term in q_terms:
        entry = vocab.get(term)
        if not entry:
            continue
        df = entry["df"]
        idf = math.log(1 + (n_docs - df + 0.5) / (df + 0.5))
        for chunk_id, count in entry["postings"]:
            dl = max(1, docs[chunk_id].get("dl", 1))
            denom = count + k1 * (1 - b + b * dl / avg_dl)
            scores[chunk_id] += idf * (count * (k1 + 1)) / denom
            matched[chunk_id].add(term)
    ranked = sorted(scores.items(), key=lambda item: item[1], reverse=True)[:bm25_top]
    hits: list[SearchHit] = []
    seen_pages: set[str] = set()
    for ranked_item in rerank_chunks(vault, query, ranked, mode=rerank_mode, allow_remote_ollama=allow_remote_ollama):
        chunk_id = ranked_item["chunk_id"]
        score = float(ranked_item["score"])
        doc = docs[chunk_id]
        page_path = doc["page_path"]
        if page_path in seen_pages:
            continue
        payload = load_chunk_payload(vault, doc["path"]) or {}
        page = vault / page_path
        try:
            info = analyze_note(vault, page)
        except Exception:
            continue
        source_text = str(payload.get("raw_text") or "")
        hits.append(SearchHit(
            path=page_path,
            title=info.title,
            score=round(score, 4),
            matched_terms=sorted(matched[chunk_id])[:16],
            summary=info.summary,
            snippet=best_snippet(source_text, sorted(matched[chunk_id])),
            chunk_id=chunk_id,
            rerank_score=round(score, 4),
            rerank_source=str(ranked_item["source"]),
        ))
        seen_pages.add(page_path)
        if len(hits) >= top_k:
            break
    return hits


def rerank_chunks(
    vault: Path,
    query: str,
    ranked: list[tuple[str, float]],
    mode: str = "auto",
    allow_remote_ollama: bool = False,
) -> list[dict[str, object]]:
    if mode not in {"auto", "lexical", "ollama", "none"}:
        mode = "auto"
    if mode == "none":
        return [{"chunk_id": cid, "score": score, "source": "bm25-chunk"} for cid, score in ranked]
    if mode in {"auto", "ollama"}:
        ollama = rerank_chunks_ollama(vault, query, ranked, allow_remote=allow_remote_ollama)
        if ollama:
            return ollama
        if mode == "ollama":
            return [{"chunk_id": cid, "score": score, "source": "bm25-chunk+ollama-unavailable"} for cid, score in ranked]
    return rerank_chunks_lexical(vault, query, ranked)


def rerank_chunks_lexical(vault: Path, query: str, ranked: list[tuple[str, float]]) -> list[dict[str, object]]:
    q_terms = set(tokenize(query))
    if not q_terms:
        return [{"chunk_id": cid, "score": score, "source": "bm25-chunk+lexical-rerank"} for cid, score in ranked]
    index = load_json(meta_dir(vault) / "bm25" / "index.json", {})
    docs = index.get("docs", {})
    rescored: list[dict[str, object]] = []
    for chunk_id, score in ranked:
        doc = docs.get(chunk_id, {})
        payload = load_chunk_payload(vault, doc.get("path", "")) or {}
        text = str(payload.get("contextualized_text") or payload.get("raw_text") or "")
        tokens = set(tokenize(text))
        overlap = len(q_terms & tokens) / max(1, len(q_terms))
        title = str(payload.get("page_title") or "").lower()
        title_boost = 0.15 if any(term in title for term in q_terms) else 0.0
        rescored.append({
            "chunk_id": chunk_id,
            "score": score * (1.0 + overlap + title_boost),
            "source": "bm25-chunk+lexical-rerank",
        })
    return sorted(rescored, key=lambda item: float(item["score"]), reverse=True)


def cosine(a: list[float], b: list[float]) -> float:
    if not a or not b or len(a) != len(b):
        return 0.0
    dot = sum(x * y for x, y in zip(a, b))
    na = math.sqrt(sum(x * x for x in a))
    nb = math.sqrt(sum(y * y for y in b))
    if na == 0 or nb == 0:
        return 0.0
    return dot / (na * nb)


def ollama_base_url(allow_remote: bool = False) -> str | None:
    url = os.environ.get("OLLAMA_URL", DEFAULT_OLLAMA_URL).rstrip("/")
    parsed = urllib.parse.urlparse(url)
    host = parsed.hostname or ""
    if not allow_remote and host not in {"127.0.0.1", "localhost", "::1"}:
        return None
    return url


def ollama_models(url: str) -> set[str] | None:
    try:
        req = urllib.request.Request(f"{url}/api/tags", method="GET")
        with urllib.request.urlopen(req, timeout=OLLAMA_TIMEOUT_SEC) as resp:
            data = json.loads(resp.read(MAX_OLLAMA_RESPONSE_BYTES))
    except (urllib.error.URLError, TimeoutError, OSError, json.JSONDecodeError):
        return None
    names = set()
    for model in data.get("models", []):
        name = str(model.get("name", ""))
        if name:
            names.add(name)
            names.add(name.split(":")[0])
    return names


def ollama_embed(url: str, model: str, text: str) -> list[float] | None:
    payload = json.dumps({"model": model, "prompt": text}).encode("utf-8")
    req = urllib.request.Request(
        f"{url}/api/embeddings",
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=EMBED_TIMEOUT_SEC) as resp:
            data = json.loads(resp.read(MAX_OLLAMA_RESPONSE_BYTES))
    except (urllib.error.URLError, TimeoutError, OSError, json.JSONDecodeError):
        return None
    emb = data.get("embedding")
    if not isinstance(emb, list):
        return None
    return [float(x) for x in emb if isinstance(x, (int, float))]


def load_embed_cache(vault: Path) -> dict[str, list[float]]:
    raw = load_json(meta_dir(vault) / "embed-cache.json", {})
    if not isinstance(raw, dict):
        return {}
    return {str(k): v for k, v in raw.items() if isinstance(v, list)}


def save_embed_cache(vault: Path, cache: dict[str, list[float]]) -> None:
    save_json(meta_dir(vault) / "embed-cache.json", cache)


def rerank_chunks_ollama(
    vault: Path,
    query: str,
    ranked: list[tuple[str, float]],
    allow_remote: bool = False,
) -> list[dict[str, object]] | None:
    url = ollama_base_url(allow_remote)
    if not url:
        return None
    models = ollama_models(url)
    if not models or DEFAULT_OLLAMA_MODEL not in models:
        return None
    q_emb = ollama_embed(url, DEFAULT_OLLAMA_MODEL, query)
    if not q_emb:
        return None
    index = load_json(meta_dir(vault) / "bm25" / "index.json", {})
    docs = index.get("docs", {})
    cache = load_embed_cache(vault)
    dirty = False
    rescored: list[dict[str, object]] = []
    for chunk_id, bm25_score in ranked:
        doc = docs.get(chunk_id, {})
        payload = load_chunk_payload(vault, doc.get("path", "")) or {}
        text = str(payload.get("contextualized_text") or payload.get("raw_text") or "")
        body_hash = str(payload.get("body_hash") or file_hash(text))
        cache_key = f"{DEFAULT_OLLAMA_MODEL}:{body_hash}"
        emb = cache.get(cache_key)
        if not emb:
            emb = ollama_embed(url, DEFAULT_OLLAMA_MODEL, text)
            if not emb:
                rescored.append({
                    "chunk_id": chunk_id,
                    "score": bm25_score,
                    "source": "bm25-chunk+ollama-embed-miss",
                })
                continue
            cache[cache_key] = emb
            dirty = True
        semantic = cosine(q_emb, emb)
        # Keep sparse relevance in the score so semantic near-misses do not dominate.
        final_score = bm25_score * (1.0 + max(0.0, semantic))
        rescored.append({
            "chunk_id": chunk_id,
            "score": final_score,
            "source": f"bm25-chunk+ollama:{DEFAULT_OLLAMA_MODEL}",
        })
    if dirty:
        save_embed_cache(vault, cache)
    return sorted(rescored, key=lambda item: float(item["score"]), reverse=True)


def retrieve(
    vault: Path,
    query: str,
    top_k: int = 8,
    rerank_mode: str = "auto",
    allow_remote_ollama: bool = False,
) -> list[SearchHit]:
    chunk_hits = query_chunk_index(
        vault,
        query,
        top_k=top_k,
        rerank_mode=rerank_mode,
        allow_remote_ollama=allow_remote_ollama,
    )
    if chunk_hits:
        return chunk_hits
    docs = load_docs(vault)
    q_tokens = tokenize(query)
    if not q_tokens:
        return []
    q_counts = Counter(q_tokens)
    doc_tokens = []
    df = Counter()
    for _, text, info in docs:
        weighted = " ".join([
            info.title * 3,
            " ".join(info.tags) * 2,
            text,
        ])
        toks = tokenize(weighted)
        doc_tokens.append(toks)
        df.update(set(toks))
    N = max(1, len(docs))
    avgdl = sum(len(t) for t in doc_tokens) / N if docs else 1
    k1, b = 1.5, 0.75
    hits: list[SearchHit] = []
    for (path, text, info), toks in zip(docs, doc_tokens):
        tf = Counter(toks)
        dl = max(1, len(toks))
        score = 0.0
        matched = []
        for term, qtf in q_counts.items():
            if tf[term] <= 0:
                continue
            matched.append(term)
            idf = math.log(1 + (N - df[term] + 0.5) / (df[term] + 0.5))
            denom = tf[term] + k1 * (1 - b + b * dl / max(1, avgdl))
            score += idf * ((tf[term] * (k1 + 1)) / denom) * qtf
        # Boost AI-first high-value areas.
        r = rel(vault, path)
        if r.startswith("wiki/projects/") or r.startswith("projects/"):
            score *= 1.15
        if r.startswith("wiki/decisions/") or r.startswith("decisions/"):
            score *= 1.12
        if r.startswith("wiki/sources/"):
            score *= 0.92
        if score > 0:
            hits.append(SearchHit(
                path=r,
                title=info.title,
                score=round(score, 4),
                matched_terms=matched[:16],
                summary=info.summary,
                snippet=best_snippet(text, matched),
            ))
    hits.sort(key=lambda h: h.score, reverse=True)
    return hits[:top_k]


def best_snippet(text: str, terms: Sequence[str], max_chars: int = 420) -> str:
    body = strip_frontmatter(text)
    lower = body.lower()
    pos = None
    for t in terms:
        idx = lower.find(t.lower())
        if idx >= 0:
            pos = idx if pos is None else min(pos, idx)
    if pos is None:
        return extract_summary(text, max_chars=max_chars)
    start = max(0, pos - max_chars // 3)
    end = min(len(body), start + max_chars)
    snippet = body[start:end]
    snippet = re.sub(r"\s+", " ", snippet).strip()
    if start > 0:
        snippet = "..." + snippet
    if end < len(body):
        snippet += "..."
    return snippet


