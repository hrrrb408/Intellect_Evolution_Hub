def now() -> dt.datetime:
    return dt.datetime.now().astimezone()


def today() -> str:
    return now().strftime(DATE_FMT)


def slugify(text: str, max_len: int = 72) -> str:
    text = text.strip().lower()
    text = re.sub(r"https?://", "", text)
    text = re.sub(r"[^a-z0-9\u4e00-\u9fff]+", "-", text)
    text = re.sub(r"-+", "-", text).strip("-")
    if not text:
        text = "note"
    return text[:max_len].strip("-") or "note"


def resolve_vault(path: str | None = None) -> Path:
    raw = path or os.environ.get("OBSIDIAN_VAULT_PATH") or os.getcwd()
    p = Path(raw).expanduser().resolve()
    p.mkdir(parents=True, exist_ok=True)
    return p


def rel(vault: Path, path: Path) -> str:
    try:
        return path.resolve().relative_to(vault.resolve()).as_posix()
    except Exception:
        return path.as_posix()


def safe_read(path: Path, limit_bytes: int | None = None) -> str:
    data = path.read_bytes()
    if limit_bytes is not None:
        data = data[:limit_bytes]
    for enc in ("utf-8", "utf-8-sig", "latin-1"):
        try:
            return data.decode(enc)
        except UnicodeDecodeError:
            continue
    return data.decode("utf-8", errors="replace")


def lock_root_for(path: Path) -> Path:
    root = path.parent.resolve()
    while root != root.parent:
        if (root / "wiki").exists() or (root / ".obsidian").exists() or (root / ".git").exists():
            return root / ".compound-locks"
        root = root.parent
    return path.parent.resolve() / ".compound-locks"


def lock_dir_for(path: Path) -> Path:
    digest = hashlib.sha256(str(path.resolve()).encode("utf-8")).hexdigest()
    return lock_root_for(path) / f"{digest}.lock"


def lock_is_stale(lock_dir: Path) -> bool:
    pid_file = lock_dir / "pid"
    if not pid_file.exists():
        return True
    age = time.time() - pid_file.stat().st_mtime
    return age > LOCK_STALE_SECONDS


@contextmanager
def file_lock(path: Path) -> Iterator[None]:
    lock_dir = lock_dir_for(path)
    lock_dir.parent.mkdir(parents=True, exist_ok=True)
    start = time.time()
    while True:
        try:
            lock_dir.mkdir()
            (lock_dir / "pid").write_text(str(os.getpid()), encoding="utf-8")
            (lock_dir / "target").write_text(str(path), encoding="utf-8")
            break
        except FileExistsError:
            if lock_is_stale(lock_dir):
                shutil.rmtree(lock_dir, ignore_errors=True)
                continue
            if time.time() - start >= LOCK_TIMEOUT_SECONDS:
                raise TimeoutError(f"timed out waiting for lock: {path}")
            time.sleep(LOCK_SLEEP_SECONDS)
    try:
        yield
    finally:
        shutil.rmtree(lock_dir, ignore_errors=True)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with file_lock(path):
        tmp = path.with_suffix(path.suffix + ".tmp")
        tmp.write_text(text, encoding="utf-8")
        tmp.replace(path)


def append_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with file_lock(path):
        with path.open("a", encoding="utf-8") as f:
            f.write(text)


def is_under_nested_git_repo(vault: Path, path: Path) -> bool:
    vault_resolved = vault.resolve()
    current = path.resolve().parent
    while current != vault_resolved and current != current.parent:
        if (current / ".git").exists():
            return True
        current = current.parent
    return False


def iter_markdown(vault: Path, include_generated: bool = True) -> Iterator[Path]:
    for path in vault.rglob("*.md"):
        if is_under_nested_git_repo(vault, path):
            continue
        if path.name in SKIP_FILES and path.parent.resolve() == vault.resolve():
            continue
        parts = set(path.relative_to(vault).parts)
        if parts & SKIP_DIRS:
            continue
        if not include_generated:
            r = rel(vault, path)
            if r in {"wiki/index.md", "wiki/hot.md", "wiki/log.md"} or r.startswith("wiki/meta/"):
                continue
            if is_singularity_mode(vault) and r.startswith("wiki/resources/"):
                continue
            try:
                if GENERATED_MARKER in safe_read(path, limit_bytes=4096):
                    continue
            except Exception:
                continue
        yield path


def tokenize(text: str) -> list[str]:
    tokens = [m.group(0).lower() for m in WORD_RE.finditer(text)]
    # keep English-ish words and single CJK chars; filter very tiny ASCII noise
    return [t for t in tokens if len(t) > 1 or re.match(r"[\u4e00-\u9fff]", t)]


def strip_frontmatter(text: str) -> str:
    return FRONTMATTER_RE.sub("", text, count=1)


def extract_title(path: Path, text: str) -> str:
    for line in strip_frontmatter(text).splitlines():
        if line.startswith("# "):
            return line[2:].strip()
    return path.stem


def extract_frontmatter(text: str) -> dict[str, str]:
    m = FRONTMATTER_RE.match(text)
    if not m:
        return {}
    data: dict[str, str] = {}
    for line in m.group(1).splitlines():
        if ":" in line:
            k, v = line.split(":", 1)
            data[k.strip()] = v.strip().strip('"')
    return data


def extract_summary(text: str, max_chars: int = 240) -> str:
    body = strip_frontmatter(text)
    lines = []
    for line in body.splitlines():
        s = line.strip()
        if not s or s.startswith("#") or s.startswith("---") or s.startswith("!"):
            continue
        if s.startswith("<!--"):
            continue
        lines.append(s)
        if sum(len(x) for x in lines) >= max_chars:
            break
    summary = " ".join(lines)
    return summary[:max_chars].strip()


def file_hash(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8", errors="ignore")).hexdigest()[:16]


def bytes_hash(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()[:16]


def meta_dir(vault: Path) -> Path:
    return vault / ".vault-meta"


def manifest_path(vault: Path) -> Path:
    return meta_dir(vault) / "compound-manifest.json"


def load_json(path: Path, default):
    if not path.exists():
        return default
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return default


def save_json(path: Path, payload) -> None:
    write_text(path, json.dumps(payload, ensure_ascii=False, indent=2) + "\n")


def load_manifest(vault: Path) -> dict[str, object]:
    manifest = load_json(manifest_path(vault), {"schema_version": 1, "sources": {}})
    if "sources" not in manifest or not isinstance(manifest["sources"], dict):
        manifest["sources"] = {}
    return manifest


def save_manifest(vault: Path, manifest: dict[str, object]) -> None:
    save_json(manifest_path(vault), manifest)


def mode_path(vault: Path) -> Path:
    return meta_dir(vault) / "mode.json"


def mode_config(vault: Path) -> dict[str, object]:
    cfg = load_json(mode_path(vault), {})
    mode = cfg.get("mode", "generic") if isinstance(cfg, dict) else "generic"
    if mode not in VALID_MODES:
        mode = "generic"
    return {"mode": mode, "configured_at": cfg.get("configured_at") if isinstance(cfg, dict) else None}


def set_mode(vault: Path, mode: str) -> dict[str, object]:
    if mode not in VALID_MODES:
        raise ValueError(f"invalid mode: {mode}")
    payload = {
        "schema_version": 1,
        "mode": mode,
        "configured_at": now().isoformat(),
    }
    save_json(mode_path(vault), payload)
    return payload


def safe_title(text: str, fallback: str = "untitled") -> str:
    cleaned = re.sub(r"[/\\\x00-\x1f]+", " ", text).strip()
    cleaned = cleaned.lstrip(".-").strip()
    return cleaned[:96] or fallback


def is_singularity_mode(vault: Path) -> bool:
    return mode_config(vault)["mode"] == "singularity"


