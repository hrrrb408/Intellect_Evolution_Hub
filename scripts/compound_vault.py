        "",
        "## Summary",
        "",
        f"- Notes: {report['note_count']}",
        f"- Dead links: {len(dead_links)}",
        f"- Orphan pages: {len(orphan_pages)}",
        f"- Missing frontmatter: {len(missing_frontmatter)}",
        f"- Missing AI-first marker: {len(missing_ai_first)}",
        f"- Duplicate titles: {len(duplicate_titles)}",
        f"- Index missing paths: {len(index_missing)}",
