def write_rewrite_plan(
    vault: Path,
    source_rel: str,
    text: str,
    distributed: dict[str, list[str]],
    analysis: dict[str, object] | None = None,
) -> str:
    source_title = extract_title(vault / source_rel, text)
    tokens = Counter(tokenize(text))
    top_terms = [term for term, _ in tokens.most_common(25)]
    relevant_hits = retrieve(vault, " ".join(top_terms[:8]), top_k=8, rerank_mode="lexical")
    plan_path = vault / "wiki/meta/rewrite-plan-latest.md"
    lines = [
        "---",
        f"title: Rewrite Plan for {source_title.replace(chr(34), chr(39))}",
        "type: rewrite-plan",
        f"created: {now().strftime(DATETIME_FMT)}",
        "ai-first: true",
        "---",
        "",
        "## For future Claude",
        "",
        "This generated plan is a work queue for rewriting durable notes after source ingest. Verify every candidate against the source note before editing.",
        "",
        f"# Rewrite Plan: {source_title}",
        "",
        GENERATED_MARKER,
        "",
        "## Source",
        "",
        f"- Source note: `{source_rel}`",
        f"- Wikilink: {path_to_wikilink(source_rel)}",
        "",
        "## Deterministic Candidate Stubs",
        "",
        "### Entities",
        "",
    ]
    lines += [f"- `{p}`" for p in distributed.get("entities", [])] or ["None."]
    lines += ["", "### Concepts", ""]
    lines += [f"- `{p}`" for p in distributed.get("concepts", [])] or ["None."]
    lines += ["", "## Existing Notes to Review", ""]
    if relevant_hits:
        for hit in relevant_hits:
            if hit.path == source_rel or not is_safe_patch_target_path(hit.path):
                continue
            lines.append(f"- `{hit.path}` - {hit.title} - score {hit.score} - {hit.summary}")
    else:
        lines.append("None.")
    lines += [
        "",
        "## Generated Analysis Artifacts",
        "",
    ]
    paths = (analysis or {}).get("paths", {}) if isinstance(analysis, dict) else {}
    if paths:
        for label, artifact_path in paths.items():
            lines.append(f"- {label}: `{artifact_path}`")
    else:
        lines.append("None.")
    lines += [
        "",
        "## Rewrite Instructions",
        "",
        "1. Read the source note first.",
        "2. Read `wiki/meta/source-claims-latest.json` and `wiki/meta/patch-proposals-latest.md`.",
        "3. Read each candidate durable note above.",
        "4. Patch the smallest section that can absorb the new evidence.",
        "5. Preserve old claims in a History or Timeline section when they changed.",
        "6. Add source wikilinks and confidence labels.",
        "7. Do not treat deterministic stubs as verified until the source supports them.",
        "",
        "## Top Terms",
        "",
        ", ".join(top_terms[:25]) or "None.",
    ]
    write_text(plan_path, "\n".join(lines).rstrip() + "\n")
    return rel(vault, plan_path)


