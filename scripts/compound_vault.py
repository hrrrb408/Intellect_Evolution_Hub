def tail_lines(path: Path, n: int) -> list[str]:
    if not path.exists():
        return []
    lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    lines = [x for x in lines if x.strip().startswith("-")]
    return lines[-n:]


