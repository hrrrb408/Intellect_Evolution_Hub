def build_link_index(vault: Path) -> tuple[dict[str, Path], dict[str, set[str]], dict[str, set[str]]]:
    aliases: dict[str, Path] = {}
    outlinks: dict[str, set[str]] = defaultdict(set)
    inlinks: dict[str, set[str]] = defaultdict(set)
    for p in iter_markdown(vault, include_generated=False):
        r = rel(vault, p)
        text = safe_read(p)
        aliases[Path(r).stem.lower()] = p
        aliases[Path(r).with_suffix("").as_posix().lower()] = p
        fm = extract_frontmatter(text)
        if fm.get("title"):
            aliases[fm["title"].lower()] = p
    for r in ("wiki/index.md", "wiki/hot.md", "wiki/log.md"):
        p = vault / r
        if p.exists():
            aliases[Path(r).stem.lower()] = p
            aliases[Path(r).with_suffix("").as_posix().lower()] = p
    for p in iter_markdown(vault, include_generated=False):
        r = rel(vault, p)
        text = safe_read(p)
        links = set(x.strip() for x in WIKILINK_RE.findall(text) if x.strip())
        outlinks[r] |= links
        for link in links:
            target_key = link.lower()
            if target_key in aliases:
                inlinks[rel(vault, aliases[target_key])].add(r)
            else:
                # try stem only
                stem_key = Path(link).stem.lower()
                if stem_key in aliases:
                    inlinks[rel(vault, aliases[stem_key])].add(r)
    return aliases, outlinks, inlinks


def health_report(vault: Path) -> dict[str, object]:
    ensure_scaffold(vault)
    note_paths = list(iter_markdown(vault, include_generated=False))
    notes = [analyze_note(vault, p) for p in note_paths]
    aliases, outlinks, inlinks = build_link_index(vault)
    dead_links = []
    for src, links in outlinks.items():
        for link in sorted(links):
            if link.lower() not in aliases and Path(link).stem.lower() not in aliases:
                dead_links.append({"source": src, "link": link})
    orphan_pages = []
    for n in notes:
        if n.note_type in {"system", "log", "index", "hot-cache", "meta"}:
            continue
        if n.path.startswith("wiki/sources/") or n.path.startswith("wiki/meta/"):
            continue
        if not n.links and not inlinks.get(n.path):
            orphan_pages.append(n.path)
    missing_frontmatter = []
    missing_ai_first = []
    for p in note_paths:
        sample = safe_read(p, limit_bytes=4096)
        fm = extract_frontmatter(sample)
        if not FRONTMATTER_RE.match(sample):
            missing_frontmatter.append(rel(vault, p))
        elif fm.get("ai-first") != "true" and not rel(vault, p).startswith("wiki/sources/"):
            missing_ai_first.append(rel(vault, p))
    title_map: dict[str, list[str]] = defaultdict(list)
    for n in notes:
        if n.note_type in {"system", "log", "index", "hot-cache", "meta", "source"} or n.path.startswith("wiki/meta/"):
            continue
        title_map[n.title.lower()].append(n.path)
    duplicate_titles = {
        title: paths for title, paths in title_map.items()
        if title and len(paths) > 1
    }
    generated_staleness = {}
    for r in ["wiki/index.md", "wiki/hot.md", ".vault-meta/bm25/index.json", ".vault-meta/compound-manifest.json"]:
        p = vault / r
        if p.exists():
            age_hours = (now() - dt.datetime.fromtimestamp(p.stat().st_mtime).astimezone()).total_seconds() / 3600
            generated_staleness[r] = round(age_hours, 2)
        else:
            generated_staleness[r] = None
    index_json = vault / "wiki/meta/index.json"
    index_paths = set()
    if index_json.exists():
        try:
            index_paths = {x["path"] for x in json.loads(index_json.read_text(encoding="utf-8"))}
        except Exception:
            index_paths = set()
    actual_paths = {rel(vault, p) for p in note_paths if not retrieval_disabled(vault, p)}
    index_missing = sorted(actual_paths - index_paths) if index_paths else sorted(actual_paths)
    manifest = load_manifest(vault)
    manifest_missing = []
    pdf_extraction_issues = []
    manifest_untracked_stage_sources = untracked_stage_source_records(vault, manifest)
    for source_key, item in manifest.get("sources", {}).items():
        source_note = item.get("source_note") if isinstance(item, dict) else None
        if source_note and not (vault / source_note).exists():
            manifest_missing.append({"source": source_key, "source_note": source_note})
        if isinstance(item, dict) and item.get("source_format") == "pdf":
            extraction = item.get("pdf_extraction", {})
            status = extraction.get("status") if isinstance(extraction, dict) else None
            if status in {"ocr-required", "ocr-empty", "ocr-render-failed", "pdftotext-failed", "pdftotext-unavailable", "empty"}:
                pdf_extraction_issues.append({
                    "source": source_key,
                    "source_note": source_note,
                    "status": status,
                    "diagnostics": str(extraction.get("stderr", ""))[:500] if isinstance(extraction, dict) else "",
                })
    return {
        "generated_at": now().strftime(DATETIME_FMT),
        "note_count": len(notes),
        "dead_links": dead_links,
        "orphan_pages": sorted(orphan_pages),
        "missing_frontmatter": sorted(missing_frontmatter),
        "missing_ai_first": sorted(missing_ai_first),
        "duplicate_titles": duplicate_titles,
        "generated_staleness_hours": generated_staleness,
        "index_missing_paths": index_missing,
        "manifest_missing_sources": manifest_missing,
        "manifest_untracked_stage_sources": manifest_untracked_stage_sources,
        "pdf_extraction_issues": pdf_extraction_issues,
    }


def write_health_report(vault: Path, report: dict[str, object]) -> Path:
    date = today()
    path = vault / "wiki/meta" / f"lint-report-{date}.md"
    dead_links = report["dead_links"]
    orphan_pages = report["orphan_pages"]
    missing_frontmatter = report["missing_frontmatter"]
    missing_ai_first = report["missing_ai_first"]
    index_missing = report["index_missing_paths"]
    duplicate_titles = report["duplicate_titles"]
    manifest_missing = report["manifest_missing_sources"]
    manifest_untracked_stage_sources = report.get("manifest_untracked_stage_sources", [])
    pdf_extraction_issues = report.get("pdf_extraction_issues", [])
    lines = [
        "---",
        f"title: Vault Health Report {date}",
        "type: health-report",
        f"created: {now().strftime(DATETIME_FMT)}",
        "ai-first: true",
        "---",
        "",
        "## For future Claude",
        "",
        "This generated report lists structural issues found in the vault. Treat it as a repair queue and verify current files before making fixes.",
        "",
        f"# Vault Health Report {date}",
        "",
        GENERATED_MARKER,
        "",
        "## Summary",
        "",
        f"- Notes: {report['note_count']}",
        f"- Dead links: {len(dead_links)}",
        f"- Orphan pages: {len(orphan_pages)}",
        f"- Missing frontmatter: {len(missing_frontmatter)}",
        f"- Missing AI-first marker: {len(missing_ai_first)}",
        f"- Duplicate titles: {len(duplicate_titles)}",
        f"- Index missing paths: {len(index_missing)}",
        f"- Manifest missing sources: {len(manifest_missing)}",
        f"- Manifest untracked stage sources: {len(manifest_untracked_stage_sources)}",
        f"- PDF extraction issues: {len(pdf_extraction_issues)}",
        f"- Generated staleness hours: `{json.dumps(report['generated_staleness_hours'], ensure_ascii=False)}`",
        "",
        "## Dead Links",
        "",
    ]
    if dead_links:
        for item in dead_links[:200]:
            lines.append(f"- `{item['source']}` → `[[{item['link']}]]`")
    else:
        lines.append("None.")
    lines += ["", "## Orphan Pages", ""]
    lines += [f"- `{x}`" for x in orphan_pages[:200]] or ["None."]
    lines += ["", "## Missing Frontmatter", ""]
    lines += [f"- `{x}`" for x in missing_frontmatter[:200]] or ["None."]
    lines += ["", "## Missing AI-first Marker", ""]
    lines += [f"- `{x}`" for x in missing_ai_first[:200]] or ["None."]
    lines += ["", "## Duplicate Titles", ""]
    if duplicate_titles:
        for title, paths in list(duplicate_titles.items())[:100]:
            lines.append(f"- `{title}`: {', '.join('`' + p + '`' for p in paths)}")
    else:
        lines.append("None.")
    lines += ["", "## Index Missing Paths", ""]
    lines += [f"- `{x}`" for x in index_missing[:200]] or ["None."]
    lines += ["", "## Manifest Missing Sources", ""]
    if manifest_missing:
        for item in manifest_missing[:100]:
            lines.append(f"- `{item['source']}` -> `{item['source_note']}`")
    else:
        lines.append("None.")
    lines += ["", "## Manifest Untracked Stage Sources", ""]
    if manifest_untracked_stage_sources:
        for item in manifest_untracked_stage_sources[:100]:
            lines.append(f"- `{item['source']}` -> `{item.get('source_note', '')}`")
    else:
        lines.append("None.")
    lines += ["", "## PDF Extraction Issues", ""]
    if pdf_extraction_issues:
        for item in pdf_extraction_issues[:100]:
            lines.append(f"- `{item['source_note']}` status=`{item['status']}` diagnostics=`{item['diagnostics']}`")
    else:
        lines.append("None.")
    text = "\n".join(lines).rstrip() + "\n"
    write_text(path, text)
    write_text(vault / "wiki/meta/lint-report-latest.md", text)
    return path


def cmd_health(args: argparse.Namespace) -> int:
    vault = resolve_vault(args.vault)
    report = health_report(vault)
    path = write_health_report(vault, report)
    if args.fix_index:
        build_index(vault)
        update_hot(vault)
    log_event(vault, "compound-health", f"report -> {rel(vault, path)}")
    if args.json:
        print(json.dumps(report, ensure_ascii=False, indent=2))
    else:
        print(f"Health report written to {path}")
        print(f"Dead links: {len(report['dead_links'])}")
        print(f"Orphan pages: {len(report['orphan_pages'])}")
        print(f"Missing frontmatter: {len(report['missing_frontmatter'])}")
        print(f"Missing AI-first marker: {len(report['missing_ai_first'])}")
    return 0


