def cmd_apply_proposals(args: argparse.Namespace) -> int:
    vault = resolve_vault(args.vault)
    ensure_scaffold(vault)
    proposal_path = Path(args.proposal_file).expanduser() if args.proposal_file else vault / "wiki/meta/patch-proposals-latest.json"
    if not proposal_path.is_absolute():
        proposal_path = vault / proposal_path
    proposal_path = proposal_path.resolve()
    try:
        proposal_path.relative_to(vault.resolve())
    except ValueError:
        print(f"Proposal file must be inside vault: {proposal_path}", file=sys.stderr)
        return 2
    proposals = load_json(proposal_path, [])
    if not isinstance(proposals, list):
        print(f"Proposal file is not a list: {proposal_path}", file=sys.stderr)
        return 2

    dry_run = not args.apply
    applied = 0
    items: list[dict[str, str]] = []
    for proposal in proposals:
        if not isinstance(proposal, dict):
            continue
        proposal_id = str(proposal.get("proposal_id") or "unknown")
        action = str(proposal.get("action") or "")
        target_path = str(proposal.get("target_path") or "")
        proposed_text = str(proposal.get("proposed_text") or "").strip()
        item = {
            "proposal_id": proposal_id,
            "action": action,
            "target_path": target_path,
            "status": "skipped",
            "reason": "",
        }
        if action == "review_contradiction":
            item["reason"] = "contradiction review requires manual confirmation"
            items.append(item)
            continue
        if action not in {"append_evidence", "append_timeline"}:
            item["reason"] = f"unsupported action: {action}"
            items.append(item)
            continue
        target = resolve_patch_target(vault, target_path)
        if target is None:
            item["reason"] = "unsafe target path"
            items.append(item)
            continue
        if not target.exists():
            item["reason"] = "target file missing; create or review manually first"
            items.append(item)
            continue
        if not proposed_text:
            item["reason"] = "empty proposed_text"
            items.append(item)
            continue
        current = safe_read(target)
        if proposed_text in current:
            item["reason"] = "proposal text already present"
            items.append(item)
            continue

        heading = "Timeline" if action == "append_timeline" else "Evidence"
        item["status"] = "would-apply" if dry_run else "applied"
        item["reason"] = f"append under ## {heading}"
        if not dry_run:
            updated = append_under_heading(current, heading, proposed_text)
            write_text(target, updated)
            applied += 1
        items.append(item)

    report = {
        "generated_at": now().strftime(DATETIME_FMT),
        "dry_run": dry_run,
        "proposal_file": rel(vault, proposal_path),
        "applied": applied,
        "skipped": sum(1 for item in items if item["status"] == "skipped"),
        "items": items,
    }
    write_apply_proposals_report(vault, report)
    if applied:
        build_index(vault)
        build_chunk_index(vault)
        update_hot(vault)
    log_event(vault, "compound-apply-proposals", f"dry_run={dry_run}; applied={applied}; skipped={report['skipped']}")
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


def cmd_mode(args: argparse.Namespace) -> int:
    vault = resolve_vault(args.vault)
    ensure_scaffold(vault)
    if args.mode_cmd == "get":
        print(mode_config(vault)["mode"])
