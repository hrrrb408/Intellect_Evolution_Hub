def cmd_fusion(args: argparse.Namespace) -> int:
    vault = resolve_vault(args.vault)
    ensure_scaffold(vault)
    source_rel = args.source or latest_manifest_source(vault)
    if not source_rel:
        print("No source provided and manifest has no source_note.", file=sys.stderr)
        return 2
    source_rel = rel(vault, (vault / source_rel).resolve()) if not Path(source_rel).is_absolute() else rel(vault, Path(source_rel))
    source_path = vault / source_rel
    if not source_path.exists():
        print(f"Source note not found: {source_rel}", file=sys.stderr)
        return 2
    report = build_fusion_proposals(vault, source_rel)
    drafts = build_fusion_drafts(vault, report)
    created = 0
    upgraded = 0
    moc_links = 0
    write_results: dict[str, str] = {}
    if args.apply:
        for draft in drafts:
            result = create_fusion_page(vault, draft, upgrade_scaffolds=args.upgrade_scaffolds)
            if result:
                write_results[str(draft["target_path"])] = result
            if result == "created":
                created += 1
            elif result == "upgraded":
                upgraded += 1
        sync_fusion_proposal_statuses(report, drafts, write_results)
        moc_rel = stage_path("moc", str(report["domain"]), str(report["subdomain"]), "index")
        moc_links = append_moc_links(vault, moc_rel, report["proposals"])
        if created or upgraded or moc_links:
            build_index(vault)
            build_chunk_index(vault)
            update_hot(vault)
    report["dry_run"] = not args.apply
    report["created"] = created
    report["upgraded"] = upgraded
    report["moc_links_added"] = moc_links
    report["drafts"] = [
        {k: v for k, v in draft.items() if k != "body"}
        for draft in drafts
    ]
    write_fusion_drafts_report(vault, report, drafts)
    write_fusion_report(vault, report)
    log_event(vault, "compound-fusion", f"source={source_rel}; dry_run={not args.apply}; created={created}; upgraded={upgraded}; moc_links={moc_links}")
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


