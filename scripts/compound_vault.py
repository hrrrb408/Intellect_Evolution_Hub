def is_fusion_scaffold(text: str) -> bool:
    return (
        "created by the Compound Vault fusion workflow" in text
        or "TODO: explain the concept" in text
        or "TODO: identify why this entity matters" in text
        or "TODO: write the current learning judgment" in text
    )


def build_fusion_drafts(vault: Path, report: dict[str, object]) -> list[dict[str, object]]:
    context = source_context_for_fusion(vault, str(report["source"]))
    drafts: list[dict[str, object]] = []
    for proposal in report["proposals"]:
        body = build_fusion_page_body(proposal, report, context)
        if not body:
            continue
        target_path = str(proposal["target_path"])
        target = vault / target_path
        existing = safe_read(target) if target.exists() else ""
        if not target.exists():
            apply_status = "create"
        elif is_fusion_scaffold(existing):
            apply_status = "upgrade-scaffold"
        else:
            apply_status = "manual-review-existing-note"
        drafts.append({
            "proposal_id": proposal["proposal_id"],
            "action": proposal["action"],
            "target_path": target_path,
            "apply_status": apply_status,
            "body": body,
        })
    return drafts


def create_fusion_page(vault: Path, draft: dict[str, object], upgrade_scaffolds: bool = False) -> str:
    target = vault / str(draft["target_path"])
    if not target.exists():
        write_text(target, str(draft["body"]))
        return "created"
    if upgrade_scaffolds and is_fusion_scaffold(safe_read(target)):
        write_text(target, str(draft["body"]))
        return "upgraded"
    return ""


